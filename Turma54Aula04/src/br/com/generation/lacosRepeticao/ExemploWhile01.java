package br.com.generation.lacosRepeticao;

public class ExemploWhile01 {

	public static void main(String[] args) throws InterruptedException {
		//While -->enquanto em portugol
		int contador = 0;
		while (contador <=10) {
			//msg,calculo,invadir a nasa kk ,N coisas
		System.out.println("Repetição: "+contador);
			contador++;
			Thread.sleep(500);
		}
	}
}
